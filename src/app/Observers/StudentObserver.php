    /**
     * Handle the Student "created" event.
     *
     * @param  \App\Models\Student  $student
     * @return void
     */
    public function created(Student $student)
    {
        $code = 10000;
        $previousRow = Student::where('id', '<', $student->id)
                ->orderBy('id', 'desc')
                ->first();
        if ($previousRow) {
            $code = $previousRow->code + 1;
        }
        $student->code = $code;
        $student->save();
    }

